def apply_one(ts_path: Path, lang: str) -> tuple[int, int]:
    """Return (translated_count, total_messages)."""
    text = ts_path.read_text(encoding='utf-8')
    dictionary = TRANS.get(lang, {})

    # Pattern matches <message>...<source>SRC</source>...<translation
    # type="unfinished"></translation>...</message>. Match is lazy on
    # whitespace so message bodies with <location> etc. aren't consumed.
    pattern = re.compile(
        r'(<source>(?P<src>[^<]*)</source>'
        r'(?:\s|.)*?'  # <location> etc
        r'<translation type="unfinished">)'
        r'(?P<body>[^<]*)'
        r'(</translation>)',
        re.DOTALL,
    )

    translated = 0
    total = 0

    def replace(match: re.Match) -> str:
        nonlocal translated, total
        total += 1
        src = match.group('src')
        tr = dictionary.get(src)
        if not tr:
            return match.group(0)  # leave unfinished
        translated += 1
        # Strip the `type="unfinished"` attribute so Qt treats it as done.
        head = match.group(1).replace(' type="unfinished"', '')
        tail = '</translation>'
        # Qt Linguist requires `&` in translation bodies be XML-escaped as
        # `&amp;`. Dict values use plain `&` (source-side readable) so we
        # escape here. Must also handle `<` and `>` for future safety,
        # though current dictionary values don't use them.
        tr_escaped = (tr.replace('&', '&amp;')
                        .replace('<', '&lt;')
                        .replace('>', '&gt;'))
        return f'{head}{tr_escaped}{tail}'

    new_text = pattern.sub(replace, text)
    if new_text != text:
        ts_path.write_text(new_text, encoding='utf-8')
    return translated, total


def main() -> int:
    repo = Path(__file__).resolve().parent.parent
    translations = repo / 'translations'
    langs = ['de', 'es', 'fr', 'it', 'ja', 'nl', 'pt', 'zh']
    print(f'Applying translations from {translations}/bld_<lang>.ts')
    grand_total_done = 0
    grand_total = 0
    for lang in langs:
        ts = translations / f'bld_{lang}.ts'
        if not ts.exists():
            print(f'  {lang}: {ts} missing — skipping')
            continue
        done, total = apply_one(ts, lang)
        print(f'  {lang}: {done}/{total} translated')
        grand_total_done += done
        grand_total += total
    print(f'Total: {grand_total_done}/{grand_total} translations applied.')
    return 0


if __name__ == '__main__':
    sys.exit(main())
